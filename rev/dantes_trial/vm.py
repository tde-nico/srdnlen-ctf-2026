SIZE = 0xA9
assert len(bytecode) == SIZE

def decode_byte(ip):
	return ((13 * ip + 90) ^ bytecode[ip]) & 0xFF

def decode_jmp(ip):
	lo = decode_byte(ip + 1)
	ip2 = ip + 2
	if ip2 >= SIZE:
		hi = 0
		ip = SIZE
	else:
		hi = decode_byte(ip2)
		ip += 3
	return ip, (hi << 8) | lo

